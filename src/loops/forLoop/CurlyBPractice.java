package loops.forLoop;

public class CurlyBPractice {

    public static void main(String[] args) {

        int a = 0;
        for (; a < 10; a++)
            a++;
        System.out.println(a);

        System.out.println("Summer");

        int c = 0;
        while (c < 4)
            c++;
            System.out.println(c);

    }
}
